package com.a.myfirstclass.java;

public class StringDemo {

	   public static void main(String args[]) {
	      String name = "I Love My Baby";
	      int len = name.length();
	      System.out.println( "String Length is : " + len );
	      System.out.println(name);
	      System.out.println("Lower case is = "+ name.toLowerCase());
	      System.out.println("Lower case is = "+ name.toLowerCase());
	      System.out.println("Upper case is = "+ name.toUpperCase());
	      System.out.println("To string = " + name.toString());
	      char result = name.charAt(8);//String charAt() Method
	      System.out.println(result);
	      
	   }
	}

